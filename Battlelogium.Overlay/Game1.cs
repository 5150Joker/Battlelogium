        void hook_KeyPressed(object sender, KeyPressedEventArgs e)
        {
            switch (hide)
            {
                case true:
                    ShowWindow(base.Window.Handle, ShowWindowCommands.Maximize);
                    hide = false;
                    break;
                case false:
                    ShowWindow(base.Window.Handle, ShowWindowCommands.Hide);
                    hide = true;
                    break;
            }
            
        }

